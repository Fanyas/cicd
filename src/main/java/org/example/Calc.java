package org.example;

public class Calc {
    public static int plus(int a, int b){
        return a + b;
    }
    public static int minus(int a, int b){
        return a - b;
    }
    public static double multi(int a, int b){
        return a * b;
    }
    public static double div(int a, int b){
        return a / b;
    }
}
